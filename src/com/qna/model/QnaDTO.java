package com.qna.model;

public class QnaDTO {
	private int qna_num;
	private String qna_title;
	private String qna_head;
	private int qna_hit;
	private int qna_writer;
	private String qna_regdate;
	private String qna_file;
	private String qna_text;
	
	public int getQna_num() {
		return qna_num;
	}
	
	public void setQna_num(int qna_num) {
		this.qna_num = qna_num;
	}
	
	public String getQna_title() {
		return qna_title;
	}
	
	public void setQna_title(String qna_title) {
		this.qna_title = qna_title;
	}
	
	public String getQna_head() {
		return qna_head;
	}
	
	public void setQna_head(String qna_head) {
		this.qna_head = qna_head;
	}
	
	public int getQna_hit() {
		return qna_hit;
	}
	
	public void setQna_hit(int qna_hit) {
		this.qna_hit = qna_hit;
	}
	
	public int getQna_writer() {
		return qna_writer;
	}
	
	public void setQna_writer(int qna_writer) {
		this.qna_writer = qna_writer;
	}
	
	public String getQna_regdate() {
		return qna_regdate;
	}
	
	public void setQna_regdate(String qna_regdate) {
		this.qna_regdate = qna_regdate;
	}
	
	public String getQna_file() {
		return qna_file;
	}
	
	public void setQna_file(String qna_file) {
		this.qna_file = qna_file;
	}
	
	public String getQna_text() {
		return qna_text;
	}
	
	public void setQna_text(String qna_text) {
		this.qna_text = qna_text;
	}
}
